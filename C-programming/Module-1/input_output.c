#include <stdio.h>
int main()
{
    int a;
    long long int u;
    char ch;
    printf("Enter a integer value:");
    scanf("%d", &a);
    printf("your value is %d\n", a);
    printf("Enter a long long integer value:");
    scanf("%lld", &u);
    printf("your long long int value is %lld\n", u);
    printf("Enter a character value:");
    scanf("\n%c", &ch);
    printf("your long long int value is %c\n", ch);
    return 0;
}